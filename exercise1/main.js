let x = 7;
let y = 3;

console.log("Addition (x + y):", x + y);
console.log("Subtraction (x - y):", x - y);
console.log("Multiplication (x * y):", x * y);
console.log("Division (x / y):", x / y);
console.log("Remainder (x % y):", x % y);

console.log("Post-increment (x++):", x++);
console.log("Value of x after post increment:", x);
console.log("Pre-increment (++x):", ++x);
console.log("Post-decrement (x--):", x--);
console.log("Value of x after post decrement:", x);
console.log("pre-decrement (--x):", --x);
